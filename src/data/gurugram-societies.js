function createSocietyArea(name, sectorId) {
  const baseSlug = slugify(name);
  const slug = `${baseSlug}-sec-${String(sectorId).toLowerCase()}`;
  return {
    slug,
    name,
    city: "Gurugram",
    sector: String(sectorId),
    type: "society",
    overallRating: 0,
    totalReviews: 0,
    ratings: { ...ZERO_RATINGS },
    reraComplaints: 0,
    description: `${name} in Sector ${sectorId}, Gurugram.`,
    image: pickImageForType("society", slug),
    tags: ["Society", `Sector ${sectorId}`],
    pros: [],
    cons: [],
    isSeedSociety: true,
  };
}

export const gurugramSocietyAreas = GURUGRAM_SECTOR_IDS.flatMap((sectorId) => {
  const names = KNOWN_SOCIETIES_BY_SECTOR[sectorId] || [
    `Sector ${sectorId} Residential Society`,
  ];
  return names.map((name) => createSocietyArea(name, sectorId));
});

export function getSocietiesForSector(sectorId) {
  return KNOWN_SOCIETIES_BY_SECTOR[sectorId] || [`Sector ${sectorId} Residential Society`];
}
